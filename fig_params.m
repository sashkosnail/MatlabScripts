function parameterChanged(handles)
A = str2double(handles.edit1.String);
w0 = 2*pi*str2double(handles.edit2.String);
D = handles.slider1.Value;
n0 = str2double(handles.edit4.String);
phi = deg2rad(str2double(handles.edit5.String));
Fs = str2double(handles.edit6.String);
window = str2double(handles.edit7.String);
offset = str2double(handles.edit9.String);
Ts = 1.0/Fs;
n = -n0-2*phi/w0:1:window;
ft = fittype('A*w0/sqrt(1-D^2)*exp(-D*w0*(n-n0)*Ts)*sin(w0*(n-n0)*Ts*sqrt(1-D^2)-phi)+o', ...
        'independent', 'n', 'problem', 'Ts');
y=ft(A, D, n0, offset, phi, w0, Ts, n);
plot(handles.axes1, n*Ts,y, 'r', 'LineWidth',1);
grid on; hold on
plot(handles.axes1, handles.T, handles.Data, 'b', 'LineWidth',1);
hold off
xlim([-n0-2*phi/w0 window]*Ts);
ylim(max(y)*[-1 1]);

